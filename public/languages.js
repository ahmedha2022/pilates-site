// Function to get a translated string
function getTranslation(key, replacements = {}) {
    const lang = localStorage.getItem('siteLanguage') || 'ar'; // Default to Arabic
    let text = translations[lang][key] || `{${key}}`; // Fallback to key if not found

    for (const placeholder in replacements) {
        // Ensure that {placeholder} is correctly replaced, not just substrings
        text = text.replace(new RegExp(`\\{${placeholder}\\}`, 'g'), replacements[placeholder]);
    }
    return text;
}
